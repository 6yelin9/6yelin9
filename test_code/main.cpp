#include <iostream>
using namespace std;

int main()
{
    int a, b;

    cout << "[Input]" << endl;
    cout << "a: ", cin >> a;
    cout << "b: ", cin >> b;

    cout << "[Output]" << endl;
    cout << a << " + " << b << " = " << a + b << endl;

    /*
    [Input]
    a: 10
    b: 20
    [Output]
    10 + 20 = 30
    */

    return 0;
}